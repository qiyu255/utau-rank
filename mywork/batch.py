FRAME_DIR = Path('cache/frames')
OCR_READY = Path('cache/layout')
OCR_INPUT = Path('cache/OCR-INPUT')


def hot(tid):
    aid = tid_to_aid[tid]
    cmd = ['ffmpeg', '-y', '-i', f'video/av{aid}.mp4',
           '-vf', 'fps=1', f'cache/frames/f%06d.png']
    logger.info('subprocess.run: %s', ' '.join(cmd))
    subprocess.run(cmd, check=False)


def pre_run(tid):
    logger.info('创建目录 %s', FRAME_DIR)
    FRAME_DIR.mkdir(parents=True, exist_ok=True)
    logger.info('创建目录 %s', OCR_READY)
    OCR_READY.mkdir(parents=True, exist_ok=True)
    
    return


def post_run(tid):
    target = str(OCR_INPUT/str(tid))
    logger.info('重命名 %s -> %s', OCR_READY, target)
    OCR_READY.rename(target)
    logger.info('删除目录 %s', FRAME_DIR)
    shutil.rmtree(FRAME_DIR, onexc=None)
    return


def main():
    N = len(tid_to_aid)
    n = 1
    for tid in tid_to_aid:
        logger.info('批量进度 tid=%d [%d/%d] ===========', tid, n, N)
        n += 1
        pre_run(tid)
        hot(tid)
        run.main()
        post_run(tid)
        logger.info('完成 tid=%d', tid)


if __name__ == '__main__':
    main()
